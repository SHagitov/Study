namespace Steps
{
    /// <summary>
    /// Структура, описывающая данные по месяцам
    /// </summary>
    internal struct MonthlyReport
    {
        /// <summary>
        /// Название месяца
        /// </summary>
        public string Month;

        /// <summary>
        /// Доход
        /// </summary>
        public int Income;

        /// <summary>
        /// Расход
        /// </summary>
        public int consumption;

        /// <summary>
        /// Прибыль
        /// </summary>
        public int arrived;
    }
}
